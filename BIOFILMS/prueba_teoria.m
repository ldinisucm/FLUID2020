tau0=1;
a=0;
b=0;
N=1e6;
omega=10.^[-2:.1:2];
Z=1
for i=1:length(omega)
  w=omega(i);
  %u=[tau0*w:Du:10000*tau0*w];
  u=linspace(tau0*w,100000*tau0*w,N);
  Du=(max(u)-min(u))/N;
int1=Du*sum(u.^(2-x)./(1+u.^2));
int2=Du*sum(u.^(1-x)./(1+u.^2));
a(i)=int1*w^(x-1)/Z;
b(i)=int2*w^(x-1)/Z;
%a(i)=int
end
